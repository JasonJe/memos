        response.encoding = response.apparent_encoding
        text = json.loads(response.text)
        info = text["content"]["positionResult"]["result"]
        for i in info:
            page_url    = 'https://www.lagou.com/jobs/{}.html'.format(i["positionId"]) 
            print('Get ... {}'.format(page_url))
              
