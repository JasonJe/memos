def get_proxys():
    urls = []
    for i in range(1, page_range):
        # urls.append('http://www.xicidaili.com/nn/%s'%i)
        urls.append('https://www.kuaidaili.com/free/inha/%s/'%i)
    proxys  = []
    headers = {
            'User-Agent': 'Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/62.0.3202.75 Safari/537.36'
    }
    for url in urls:
        resp = requests.get(url)
        soup = BeautifulSoup(resp.text, 'html5lib')
        ips = soup.find_all('tr')
        for i in range(1, len(ips)):
            ip = ips[i]
            tds = ip.find_all('td')
            print(tds[0].contents[0], tds[1].contents[0])
            proxys.append('http://' + tds[0].contents[0] + ':' + tds[1].contents[0])
        time.sleep(3)
    return proxys 

def get_proxy(proxys):
    return random.choice(proxys)

def get_job_details(page_url, proxys, url_start):
    headers = {
        'Host'                     : 'www.lagou.com',
        'Upgrade-Insecure-Requests': '1',
        'Accept'                   : 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng,*/*;q=0.8,application/signed-exchange;v=b3',
        'Referer'                  : quote('https://www.lagou.com/jobs/list_{}/p-city_215?&cl=false&fromSearch=true&labelWords=&suginput='.format(key_word)),
        'User-Agent'               : get_user_agent()
    }

    proxy_addr   = get_proxy(proxys)
    proxies      = {proxy_addr.split(":")[0]: proxy_addr}

    s        = requests.Session()
    s.get(url_start, headers = headers, proxies = proxies, timeout = 3)
    cookies  = s.cookies

    page_resp    = requests.get(page_url, headers = headers, proxies = proxies, timeout = 10, cookies = cookies)
    soup = BeautifulSoup(page_resp.text, "html5lib")

    job_details  = soup.find(class_="job_bt").text.replace("\n", "").strip()
    work_addr    = soup.find(class_="work_addr").text.replace("\n", "").replace(" ", "").replace("查看地图", "").strip()
    position_lng = soup.find('input', {'name' : "positionLng"}).get('value', 0)
    position_lat = soup.find('input', {'name' : "positionLat"}).get('value', 0)
    active_time  = soup.find_all('span',  {'class': "tip"})[-1].text.replace(" ", "").replace("\n", "")
    print(headers, proxies, cookies)
    return job_details, work_addr, position_lng, position_lat, active_time

